// ── Core functions ─────────────────────────────────────────────────────────

export function getLang() {
  try { return localStorage.getItem('uvidnova_lang') ?? 'en'; } catch { return 'en'; }
}

export function setLang(lang) {
  if (!LANG_META[lang]) return;
  try { localStorage.setItem('uvidnova_lang', lang); } catch { /* storage unavailable */ }
  const meta = LANG_META[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir  = meta.dir ?? 'ltr';
  document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
}

export function t(key) {
  const lang = getLang();
  return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
}

export function getName(asset) {
  const lang = getLang();
  return (lang === 'uk' && asset.name?.uk) ? asset.name.uk : (asset.name?.en ?? asset.asset_id);
}

export function applyTranslations() {
  for (const el of document.querySelectorAll('[data-i18n]')) {
    el.textContent = t(el.dataset.i18n);
  }
  for (const el of document.querySelectorAll('[data-i18n-placeholder]')) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  }
  for (const el of document.querySelectorAll('[data-i18n-aria]')) {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  }
}

// ── Language picker ────────────────────────────────────────────────────────

export function initLangToggle(btn) {
  if (!btn) return;

  // Wrap btn in a picker container
  const wrapper = document.createElement('div');
  wrapper.className = 'lang-picker';
  if (btn.classList.contains('landing-lang-toggle')) {
    wrapper.classList.add('landing-lang-picker');
  }
  btn.parentNode.insertBefore(wrapper, btn);
  wrapper.appendChild(btn);

  // Convert btn to trigger styling
  btn.classList.remove('lang-toggle', 'landing-lang-toggle');
  btn.classList.add('lang-picker-btn');

  // Build dropdown grid
  const dropdown = document.createElement('div');
  dropdown.className = 'lang-picker-dropdown';
  dropdown.setAttribute('role', 'listbox');
  wrapper.appendChild(dropdown);

  for (const [code, meta] of Object.entries(LANG_META)) {
    const opt = document.createElement('button');
    opt.className = 'lang-option';
    opt.dataset.lang = code;
    opt.setAttribute('role', 'option');
    opt.title = meta.name;
    const flagHtml = meta.flagCode
      ? flagSpan(meta.flagCode, meta.name, 'lo-flag', 16)
      : `<span class="lo-noflag">${meta.label}</span>`;
    opt.innerHTML = `${flagHtml}<span class="lo-name">${meta.name}</span>`;
    dropdown.appendChild(opt);
  }

  const syncUI = () => {
    const lang = getLang();
    const meta = LANG_META[lang] ?? LANG_META.en;
    const flagHtml = meta.flagCode
      ? flagSpan(meta.flagCode, meta.name, 'lpb-flag', 14)
      : `<span class="lpb-noflag">${meta.label}</span>`;
    btn.innerHTML = `${flagHtml}<span class="lpb-code">${meta.label}</span><span class="lpb-chevron">▾</span>`;
    for (const opt of dropdown.querySelectorAll('.lang-option')) {
      opt.classList.toggle('active', opt.dataset.lang === lang);
    }
  };

  syncUI();
  applyTranslations();

  // For pickers inside overflow-y:auto containers (landing card), use position:fixed
  const isLanding = wrapper.classList.contains('landing-lang-picker');

  function closeLandingDropdown() {
    if (!dropdown.classList.contains('open')) return;
    dropdown.classList.remove('open');
    if (isLanding && dropdown.parentNode === document.body) {
      // Return to wrapper and clear teleport styles
      wrapper.appendChild(dropdown);
      dropdown.removeAttribute('style');
    }
  }

  btn.addEventListener('click', e => {
    e.stopPropagation();
    document.querySelectorAll('.lang-picker-dropdown.open').forEach(d => {
      if (d !== dropdown) d.classList.remove('open');
    });
    const isOpening = !dropdown.classList.contains('open');
    if (isLanding && isOpening) {
      // Teleport dropdown to <body> to escape backdrop-filter stacking context
      // (backdrop-filter makes position:fixed children position relative to it, not viewport)
      const rect = wrapper.getBoundingClientRect();
      document.body.appendChild(dropdown);
      dropdown.style.position = 'fixed';
      dropdown.style.zIndex   = '99999';
      dropdown.style.minWidth = '200px';
      dropdown.style.right    = 'auto';
      dropdown.style.bottom   = 'auto';
      dropdown.style.left     = rect.left + 'px';
      // Flip upward if near bottom of viewport
      if (rect.top + 340 > window.innerHeight) {
        dropdown.style.top  = 'auto';
        dropdown.style.bottom = (window.innerHeight - rect.top + 4) + 'px';
      } else {
        dropdown.style.top  = (rect.bottom + 4) + 'px';
      }
    }
    dropdown.classList.toggle('open');
  });

  dropdown.addEventListener('click', e => {
    const opt = e.target.closest('.lang-option');
    if (!opt) return;
    setLang(opt.dataset.lang);
    closeLandingDropdown();
    dropdown.classList.remove('open');
  });

  document.addEventListener('click', () => closeLandingDropdown());
  dropdown.addEventListener('click', e => e.stopPropagation());

  document.addEventListener('langChanged', () => {
    syncUI();
    applyTranslations();
  });
}
